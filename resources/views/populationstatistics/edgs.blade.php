    <section>
        <h2>為什麼需要永續發展？</h2>
        <p>永續發展不只是關於保護環境，還涉及經濟發展與社會公平。隨著資源過度使用、氣候變遷加劇以及全球貧富差距擴大，永續發展成為解決這些挑戰的關鍵策略。透過平衡環境、經濟與社會三大支柱，我們才能確保資源能夠滿足當代與後代的需求。</p>
