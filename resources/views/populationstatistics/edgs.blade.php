        <p>永續發展目標 (SDGs) 是由聯合國於 2015 年提出的 17 項全球目標，目的是在 2030 年前消除貧窮、減少不平等、應對氣候變遷等多方面的挑戰，讓世界走向更永續的未來。</p>
